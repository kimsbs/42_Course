int		ft_count_if(char **tab, int length, int (*f)(char*))
{
	int i;
	int cnt;

	cnt = 0;
	i = -1;
	while (++i < length)
	{
		if (f(tab[i]))
			cnt++;
	}
	return (cnt);
}
