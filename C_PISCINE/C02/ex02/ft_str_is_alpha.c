int		ft_str_is_alpha(char *str)
{
	while (*str)
	{
		if ((*str <= 'z' && *str >= 'a') || (*str >= 'A' && *str <= 'Z'))
		{
			str++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
